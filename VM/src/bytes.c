/*
** big_to_little_endian()
*/

int		bytes_to_int(char *b)
{
	return ((b[0] & 0xff) << 24
			| (b[1] & 0xff) << 16
			| (b[2] & 0xff) << 8
			| (b[3] & 0xff) << 0);
}
